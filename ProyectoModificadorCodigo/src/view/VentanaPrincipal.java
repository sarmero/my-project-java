    public void remplazarPalabra(String oldPal, String newPal) {

        String[] texto = txpTexto.getText().split("\n");
        String tex = "";

        for (int i = 0; i < texto.length; i++) {
            tex = texto[i];
            System.out.println("vieja: " + texto[i]);
            texto[i] = tex.replace(oldPal, newPal);
            System.out.println("nueva: " + texto[i]);
        }
        txpTexto.setText("");
        for (int i = 0; i < texto.length; i++) {
            txpTexto.setText(txpTexto.getText() + texto[i] + "\n");
        }

    }

    public void remplazarMetodo(String oldPal) {
        String[] texto = txpTexto.getText().split("\n");
        String[] texto1 = txpTexto.getText().split("\n");
        String tex = "";
        for (int j = 0; j < palabra.size(); j++) {
            for (int i = 0; i < texto.length; i++) {
                tex = texto[i];
                texto[i] = tex.replace(oldPal, palabra.get(j));
            }
            txpTexto.setText(txpTexto.getText()  + "\n\n");
            for (int i = 0; i < texto.length; i++) {
                txpTexto.setText(txpTexto.getText() + texto[i] + "\n");
                texto[i] = texto1[i];
            }
 
        }
    }

