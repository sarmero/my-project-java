    private int[][] obtenerdatosMatriz() {

        int mat[][] = new int[9][9];
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {

                mat[j][i] = matriz[j][i];
            }
        }

        return mat;
    }

    public void llenarMatriz(int j, int i, int vec[]) {
        int n = 0;
        for (int k = j; k < (j + 3); k++) {
            for (int l = i; l < (i + 3); l++) {
                matriz[(k - 1)][(l - 1)] = vec[n];
                n++;
            }
        }

    }

