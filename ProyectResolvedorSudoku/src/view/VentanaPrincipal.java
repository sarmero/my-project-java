    private String ObtenerTexto(String tex){
        String text;
        text = (String.valueOf(numero));
        
        if (numero == '|' || numero == 'n') {
            text = "";
            if (!tex.equals("")) {
                text = tex;
            }
            
            if (numero == '|') {
                text = "";
            }
            
            
        }
        
        return text;
    }
    
    
    
    
    
    
