    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        botonEnable(0);
        coloresBotones();

        if (botonB) {
            jButtonBorrar.setBackground(colorB);
            jButtonBorrar.setForeground(colorBN);
            numero = '|';
        }

        if (!botonB) {
            jButtonBorrar.setBackground(colorB);
            jButtonBorrar.setForeground(colorBN);
            numero = '|';
            botonB = true;

        } else {
            coloresBotones();
            numero = 'n';
            botonB = false;
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

