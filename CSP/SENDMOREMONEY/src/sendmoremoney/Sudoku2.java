    private void mitem_OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mitem_OpenActionPerformed
        BufferedReader br=null;
        try {
            // TODO add your handling code here:
            JFileChooser jf=new JFileChooser("D:\\UDENAR\\2023A\\S.Expertos");
            jf.showDialog(this, "Aceptar");
            File f=jf.getSelectedFile();
            br = new BufferedReader(new FileReader(f));
            String st;
            sudoku_btns=new ArrayList<>();
            while ((st = br.readLine()) != null){
                String[] numbers=st.split(",");
                ArrayList<JButton> sudoku_row=new ArrayList<>();
                for (int i = 0; i < numbers.length; i++) {
                    JButton btn=new JButton(numbers[i]);
                    panel_Sudoku.add(btn);
                    sudoku_row.add(btn);
                }
                sudoku_btns.add(sudoku_row);                
            }
            pack();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Sudoku2.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Sudoku2.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                br.close();
            } catch (IOException ex) {
                Logger.getLogger(Sudoku2.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
    }//GEN-LAST:event_mitem_OpenActionPerformed

