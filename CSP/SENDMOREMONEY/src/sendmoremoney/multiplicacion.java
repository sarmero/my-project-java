    private void btn_SolucionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SolucionarActionPerformed
        Model model = new Model("CSP Solver");

        // Variables
        IntVar C = model.intVar("C", 0, 9);
        IntVar A = model.intVar("A", 0, 9);
        IntVar T = model.intVar("T", 0, 9);
        IntVar O = model.intVar("O", 0, 0);
        IntVar S = model.intVar("S", 0, 9);
        IntVar V = model.intVar("V", 0, 9);

        // Constraints
        model.allDifferent(C, A, T, O, S, V).post();

        IntVar cat = model.intVar("CAT", 100, 999);
        IntVar to = model.intVar("TO", 10, 99);
        IntVar tavo = model.intVar("TAVO", 1000, 9999);
        IntVar ooso = model.intVar("OOSO", 1000, 9999);

        model.times(cat, to, tavo);
        tavo.add(cat);
        
        // Restricci√≥n: TAVO + CAT = OOSO
        model.arithm(tavo, "=", ooso).post();

        // Solve
        Solver solver = model.getSolver();
        solver.solve();

        // Print solution
        System.out.println("C = " + C.getValue());
        System.out.println("A = " + A.getValue());
        System.out.println("T = " + T.getValue());
        System.out.println("O = " + O.getValue());
        System.out.println("S = " + S.getValue());
        System.out.println("V = " + V.getValue());

        String catx = " " + C.getValue() + A.getValue() + T.getValue();
        String tox = "" + T.getValue() + O.getValue();
        String tavox = "" + T.getValue() + A.getValue() + V.getValue() + O.getValue();
        String oosox = " " + O.getValue() + O.getValue() + S.getValue() + O.getValue();

        lbl_cat.setText(catx);
        lbl_to.setText(tox);
        lbl_tavo.setText(tavox);
        lbl_cat2.setText(catx);
        lbl_ooso.setText(oosox);


    }//GEN-LAST:event_btn_SolucionarActionPerformed

