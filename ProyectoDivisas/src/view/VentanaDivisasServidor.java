    private String consume(KeyEvent evt, String txt) {
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;

        if (!numeros && key != 44) {
            evt.consume();
        }

        if (key == 44) {
            for (int i = 0; i < txt.length() - 1; i++) {
                if (txt.charAt(i) == ',') {
                    evt.consume();
                }
            }
        }
        return txt;
    }
    
    

