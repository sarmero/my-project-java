    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        ErroresFormulario errores = new ErroresFormulario();

        errores.validarNumeroReal(this.txtDolar.getText());
        errores.validarNumeroReal(this.txtEuro.getText());
        errores.validarNumeroReal(this.txtLibra.getText());
        errores.validarNumeroReal(this.txtRublo.getText());
        errores.validarNumeroReal(this.txtYen.getText());

        if (!errores.hasError()) {

            double dolar = Double.parseDouble(this.txtDolar.getText());
            double euro = Double.parseDouble(this.txtEuro.getText());
            double libra = Double.parseDouble(this.txtLibra.getText());
            double rublo = Double.parseDouble(this.txtRublo.getText());
            double yen = Double.parseDouble(this.txtYen.getText());

            String[] nombres = {"Dolar", "Euro", "Libra", "Rublo", "Yen"};
            double[] valores = {dolar, euro, libra, rublo, yen};

            s.enviarInfo(nombres, valores);

        } else {
            JOptionPane.showMessageDialog(this, errores.getErrores(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnActualizarActionPerformed

