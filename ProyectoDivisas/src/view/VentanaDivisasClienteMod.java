    private void agregarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        Object[] fila = new Object[5];

        fila[0] = txtNombre.getText();
        fila[1] = txtCantidad.getText();
        fila[2] = cbxCambio.getSelectedItem().toString();
        fila[3] = txtDestino.getText();
        fila[4] = obtenerCambio();
        listDato.add(fila);
        modelo.addRow(fila);

        jTable1.setModel(modelo);
    }

    public void eliminarFila() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRowCount() - 1;
        modelo.setRowCount(0);

        listDato.remove(row);

        for (int i = 0; i < listDato.size(); i++) {
            modelo.addRow(listDato.get(i));
        }

    }

    private String obtenerCambio() {
        String cambio = "";
        try {
            // TODO add your handling code here:
            Socket client = new Socket("127.0.0.2", 8080);
            DataInputStream in = new DataInputStream(client.getInputStream());
            DataOutputStream out = new DataOutputStream(client.getOutputStream());

//            out.writeUTF("¡Hola mundo desde el cliente!");
            //Envío fraccionarios al socket
            out.writeDouble(Double.parseDouble(txtCantidad.getText()));
            out.writeInt(cbxCambio.getSelectedIndex());

            cambio = in.readUTF();

            client.close();

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "error", "Error", JOptionPane.ERROR_MESSAGE);
        }

        return cambio;

    }

