    private double centralizado() {
        int numDatos = listDato.size();
        double sum = suma(1);

        double promedio = sum / numDatos;

        double var = (suma(2) / numDatos) - Math.pow(promedio, 2);

        double desEst = Math.sqrt(var);

        return desEst;
    }

    private double decentralizado() {
        int numDatos = listDato.size();
        double sum = suma(1);
        double promedio = sum / numDatos;
        double var = (sump(Integer.valueOf(spnTarea.getValue().toString())) / numDatos) - Math.pow(promedio, 2);
        System.out.println(var);
        double desEst = Math.sqrt(var);

        return desEst;

    }

    public double sump(int n) {

        int np = listDato.size() / n;
        if (listDato.size() % n > 0) {
            n++;
        }

        Sumador[] hilos = new Sumador[n];
        int li = 0;
        int ls = np;
        for (int i = 0; i < n; i++) {
            hilos[i] = new Sumador(listDato, li, ls);
            li = ls;
            ls += np;
            if (ls > listDato.size()) {
                ls = listDato.size();
            }
        }

        for (int i = 0; i < n; i++) {
            hilos[i].start();
        }

        double sum = 0;

        for (int i = 0; i < n; i++) {
            try {
                hilos[i].join();
            } catch (InterruptedException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        for (int i = 0; i < n; i++) {
            sum += hilos[i].sum;
        }

        return sum;
    }

    private void calcularDE() {
        long ti = System.currentTimeMillis();
        double desviacion = centralizado();
        long tf = System.currentTimeMillis();
        long tt = tf - ti;
        txaPanel.setText("Centralizado: " + "\nDesviacion " + desviacion + "\nTiempo :" + tt);

        ti = System.currentTimeMillis();
        desviacion = decentralizado();
        tf = System.currentTimeMillis();
        tt = tf - ti;
        txaPanel.setText(txaPanel.getText() + "\n\nDecentralizado: " + "\nDesviacion " + desviacion + "\nTiempo :" + tt);

    }

