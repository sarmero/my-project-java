    private void AgregarDato() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        Object[] fila = new Object[2];

        fila[1] = txtDatos.getText();
        fila[0] = listDato.size() + 1;

        modelo.addRow(fila);
        jTable1.setModel(modelo);

        listDato.add(Double.valueOf(txtDatos.getText()));
        txtDatos.setText("");
    }

    private void eliminarFila() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRowCount() - 1;
        listDato.remove(row);
        modelo.setRowCount(0);
        Object[] fila = new Object[2];

        for (int i = 0; i < listDato.size(); i++) {
            fila[1] = listDato.get(i);
            fila[0] = i + 1;
            modelo.addRow(fila);
        }

        jTable1.setModel(modelo);
    }

    public double suma(int pot) {
        double sum = 0;
        for (int i = 0; i < listDato.size(); i++) {
            try {
                sum += Math.pow(listDato.get(i), pot);
                Thread.sleep(10);
            } catch (InterruptedException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return sum;
    }

