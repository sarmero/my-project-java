    private void encripted() {
        try {
            textEncryptor = new BasicTextEncryptor();
            textEncryptor.setPassword(txtKey.getText());
            myEncryptedText = textEncryptor.encrypt(txaTexto.getText());
            txaResultado.setText(myEncryptedText);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al encriptar el texto");
        }
    }
    
    private void desencripted() {
        try {
            textEncryptor = new BasicTextEncryptor();
            textEncryptor.setPassword(txtKey.getText());
            String plainText = textEncryptor.decrypt(txaTexto.getText());
            txaResultado.setText(plainText);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al desencriptar el texto");
        }
        
    }
    
    private void validarRadioBoton(boolean ban) {
        txaResultado.setText("");
        txaTexto.setText("");
        txtKey.setText("");
        
        if (ban == true) {
            rbtEncriptar.setSelected(true);
            rbtDesencriptar.setSelected(false);
            tema(ban);
        } else {
            rbtDesencriptar.setSelected(true);
            rbtEncriptar.setSelected(false);
            tema(ban);
        }
    }
    
