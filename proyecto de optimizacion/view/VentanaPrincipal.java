    private boolean buscarcaraterFinal(char ch) {
        if (ban == true) {
            if (txtRestrin.getText().length() > 0 && txtRestrin.getText().charAt(txtRestrin.getText().length() - 1) == ch) {
                return true;
            }
        } else if (txtFuncion.getText().length() > 0 && txtFuncion.getText().charAt(txtFuncion.getText().length() - 1) == ch) {
            return true;
        }
        return false;
    }

    private boolean buscarCarater(char ch) {
        int tam;
        String tex;
        if (ban == true) {
            tex = txtRestrin.getText();
        } else {
            tex = txtFuncion.getText();
        }

        tam = tex.length();

        for (int i = 0; i < tam; i++) {
            if (tex.charAt(i) == ch) {
                return true;
            }
        }

        return false;
    }


