    private String borrar(String texto) {
        String tex = "";
        int tam = texto.length() - 1;

        for (int i = 0; i < tam; i++) {
            tex = tex + texto.charAt(i);
        }

        if (tex.length() > 0 && tex.charAt(tex.length() - 1) == 'X') {
            tex = borrar(tex);
            indVar--;
        }

        return tex;
    }

    private Ecuacion validarEcuacion(String ecuacion) {

        Ecuacion ecua = new Ecuacion();
        String num = "";
        boolean val = true;
        int n = 1;

        if (ecuacion.charAt(0) == 'X') {
            num = "1";
        }

        if (ecuacion.charAt(ecuacion.length() - 2) == 'X') {
            for (int i = 0; i < ecuacion.length(); i++) {

                if (ecuacion.charAt(i) == 'X') {

                    val = true;
                    for (int j = 0; j < num.length(); j++) {

                        if (!(num.codePointAt(j) >= 43 && num.codePointAt(j) <= 57)) {
                            val = false;
                            break;
                        }
                    }

                    if (val == true) {
                        if ("-".equals(num)) {
                            num = "-1";
                        }
                        ecua.getX().add(Float.valueOf(num));
                    }

                    if (val == true && ecuacion.length() - i != 2) {

                        if ((ecuacion.charAt(i + 2) == '+' || ecuacion.charAt(i + 2) == '-')) {

                            i = i + 3;

                            num = "";
                            if (ecuacion.charAt(i) == 'X') {
                                num = "1";
                                i--;
                                val = false;
                            }
                        } else {

                            return null;
                        }
                    }

                }
                if (val == true) {
                    num = num + ecuacion.charAt(i);
                }

            }

        } else {

            return null;
        }

        return ecua;
    }

    private Ecuacion validarRestrincion(String ecuacion) {
        String num = "";
        Ecuacion ecua = null;

        if ((buscarCarater('<') == true || buscarCarater('>') == true) && buscarCarater('=') == true) {

            for (int i = 0; i < ecuacion.length(); i++) {
                if (ecuacion.charAt(i) == '<' || ecuacion.charAt(i) == '>') {

                    if (validarEcuacion(num) != null) {
                        ecua = validarEcuacion(num);

                        if (ecua.getX().size() != numVariable) {
                            return null;
                        }

                        num = "";
                        for (int j = (i + 2); j < ecuacion.length(); j++) {
                            if (!(ecuacion.charAt(j) >= 43 && ecuacion.charAt(j) <= 57)) {
                                return null;
                            } else {
                                num = num + ecuacion.charAt(j);
                            }
                        }
                        ecua.setSigno(ecuacion.charAt(i) + "" + ecuacion.charAt(i + 1) + "");
                        ecua.setResultado(Float.valueOf(num));
                        if (ecua.getResultado() < 0) {
                            return null;
                        }
                    } else {

                        return null;
                    }
                    break;
                }
                num = num + ecuacion.charAt(i);
            }
        } else {
            return null;
        }

        return ecua;
    }

