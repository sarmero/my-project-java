public DialogPedirDatos(java.awt.Frame parent, boolean modal, GrafoListas grafo) {
        super(parent, modal);
        initComponents();
         setTitle("GRAFOS NO DIRIGIDOS REPRESENTADOS COMO LISTAS LIGADAS DE ADYACENCIA");
        this.grafo=grafo;
        N=grafo.obtenerNVertices();
        M=N;
        panelBotones = new JPanel();
        panelBotones.setLayout(new FlowLayout());
        panelBotones.add(jButton1);
   
        //**************************************************
        // SE CONSTRUYE PANELMATRIZ PARA GUARDAR LOS BOTONES
        // SE COLOCA EL ADMINISTRADOR GRIDLAYOUT PARA TENER
        // UNA REGILLA PARA UBICAR LOS BOTONES 
        //**************************************************
        panelMatriz= new JPanel();
        panelMatriz.setLayout(new GridLayout(N,M));
        matrizBotones = new JToggleButton[N][M];// SE CREA LA MATRIZ DE BOTONES
        for( int i = 0 ; i < N ;i++)
        {
                for ( int j=0; j <M ; j++)
                {
	// SE CONSTRUYE EL BOTON Y SE INSERTA AL PANEL
                    matrizBotones[i][j]= new JToggleButton(""+ (i+1)+ " - "+ (j+1));
                    matrizBotones[i][j].addActionListener(this);
                    panelMatriz.add(matrizBotones[i][j]);
                    if(i== j)
                    {
                        matrizBotones[i][j].setEnabled(false);
                    }
                }
        }
        panelLetrero = new JPanel();
       
        panelLetrero.setLayout(new FlowLayout());
        letrero=new JLabel("Seleccionar si del vertice i se puede ir al vertice j");
        panelLetrero.add(letrero);
         panelTtotal= new JPanel();
          panelTtotal.setLayout(new BorderLayout());
         panelTtotal.setLayout(new BorderLayout());
        panelTtotal.add("North",panelLetrero);
        panelTtotal.add("Center",panelMatriz);
        panelTtotal.add("South",panelBotones);
        
         getContentPane().setPreferredSize(new Dimension(500,400));
         getContentPane().setLayout(new FlowLayout());
       
        getContentPane().add(panelTtotal);
    
        resize(M* 50+300,35*(N+2)+50);
        show();
    }
