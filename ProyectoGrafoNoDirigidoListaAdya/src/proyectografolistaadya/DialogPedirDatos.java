 public void actionPerformed( ActionEvent e)
    {
        
         
        if( e.getSource() instanceof JToggleButton )
          {
                
              for( int i = 0 ; i < N ;i++)
            {
                for ( int j=0; j <M ; j++)
                {
	
                    if( e.getSource() == matrizBotones[i][j] )
                    {
                       
                       matrizBotones[j][i].setSelected(     matrizBotones[i][j].isSelected()  );
                    }
                }
            }
                
           }
    }
     
