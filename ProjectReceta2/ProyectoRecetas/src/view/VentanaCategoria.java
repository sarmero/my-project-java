    private void minMoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minMoverActionPerformed
        Object[] cat = gestorListaCocina.getListaCategoria().nombreCategoria();
        Object opcion = JOptionPane.showInputDialog(
                null,
                "Â¿A que Categoria desea mover?",
                "Categoria",
                JOptionPane.QUESTION_MESSAGE,
                null,cat, cat[0]
        );
        
        if (opcion != null) {
            gestorListaCocina.getListaCategoria().moverReceta(
                    cbxCategoria.getSelectedIndex(),
                    jTable1.getSelectedRow(),
                    opcion+""
            );
            
            gestorListaCocina.mensaje("Receta movida corretamente", "Receta",0);
            obtenerReceta();
        }

        
    }//GEN-LAST:event_minMoverActionPerformed

    private void minCambiarNomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minCambiarNomActionPerformed
        String respuesta;
        respuesta = JOptionPane.showInputDialog(
                null,
                "Escriba el nombre nuevo de la categoria "+cbxCategoria.getSelectedItem(),
                JOptionPane.QUESTION_MESSAGE
        );


        if (respuesta != null) {
            if (!respuesta.equals("")) {
                gestorListaCocina.getListaCategoria().cambiarNombreCategoria(cbxCategoria.getSelectedItem()+"",respuesta);
                gestorListaCocina.mensaje("Categoria modificada corretamente", "Categoria",0);
                obtenerReceta();
            }
        }
    }//GEN-LAST:event_minCambiarNomActionPerformed

    public void obtenerReceta() {
        cbxCategoria.removeAllItems();
        gestorListaCocina.getListaCategoria().listaBoxCategoria(cbxCategoria);
        jTable1.setModel(gestorListaCocina.obtenerTablaReceta(jTable1, 0));
    }

    public void observer(Observer ven) {
        vis.addObserver(ven);
    }

    @Override
    public void update(Observable o, Object arg) {
        if (arg instanceof Boolean && vent == true) {
            setVisible(true);
            vent = false;
        }
    }

