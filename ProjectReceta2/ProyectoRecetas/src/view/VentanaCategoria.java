    private void minRemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minRemActionPerformed
        int row = jTable1.getSelectedRow();

        if (row >= 0) {
            gestorListaCocina.getListaCategoria().eliminarReceta(cbxCategoria.getSelectedIndex(), row);
            jTable1.setModel(gestorListaCocina.obtenerTablaReceta(jTable1, cbxCategoria.getSelectedIndex()));
            gestorListaCocina.mensaje("Receta removida correctamente", "Categoria",1);
        }

    }//GEN-LAST:event_minRemActionPerformed

    private void minRemAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minRemAllActionPerformed
        gestorListaCocina.getListaCategoria().eliminarRecetaAll(cbxCategoria.getSelectedIndex());
        jTable1.setModel(gestorListaCocina.obtenerTablaReceta(jTable1, cbxCategoria.getSelectedIndex()));
        gestorListaCocina.mensaje("Recetas removidas correctamente", "Categoria",1);
    }//GEN-LAST:event_minRemAllActionPerformed

    private void btnCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCategoriaActionPerformed
        String respuesta;
        respuesta = JOptionPane.showInputDialog(
                null,
                "Escriba nombre de la Categoria",
                JOptionPane.QUESTION_MESSAGE
        );

        Categoria cocina = new Categoria();

        if (respuesta != null) {
            if (!respuesta.equals("")) {
                cocina.setCategoria(respuesta);
                gestorListaCocina.getListaCategoria().agregarCategoria(cocina);
                obtenerReceta();
                gestorListaCocina.mensaje("Categoria agregada corretamente", "Categoria",0);
            }
        }
    }//GEN-LAST:event_btnCategoriaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getButton() == 3) {
            popumenu.show(jTable1, evt.getX(), evt.getY());
        }

        if (evt.getClickCount() == 2) {
            ventanaReceta.setVisible(true);
            ventanaReceta.obtenerDatos(this);
            ventanaReceta.selecionarReceta(cbxCategoria.getSelectedIndex(), jTable1.getSelectedRow());
            vent = true;
            setVisible(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

