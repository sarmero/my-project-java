    private void obtenerDatosTabla(String cliente) {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        listCodigo = new ArrayList<>();

        ArrayList<Object[]> ped = gestorListaCocina.obtenerClientePedido(cliente);

        ped.stream().forEach(pedido -> {

            Cliente cli = gestorListaCocina.getListaCliente().obtenerCliente((int) pedido[0]);
            if (cli.isVigente() == true) {
                Object[] obj = new Object[2];
                obj[0] = pedido[5];
                obj[1] = pedido[0];
                listCodigo.add(obj);

                pedido[0] = cli.getNombre();
                modelo.addRow(pedido);
            }

        });
    }

    private void mostrarCliente() {
        int row = jTable1.getSelectedRow();
        Cliente cli = gestorListaCocina.getListaCliente().obtenerCliente((int) listCodigo.get(row)[1]);
        gestorListaCocina.visualizadorImagen2(lblAvatar, cli.getAvatar());
        lblNombre.setText(cli.getNombre());
        lblCelular.setText(cli.getCelular());
        lblCorreo.setText(cli.getCorreo());
        txaDomicilio.setText(cli.getDomicilio());
    }

    public void datos(Observer obs, Usuario user) {
        vis.addObserver(obs);
        ventanaConfirmeContrase単a.setUsuario(user, this);
        cbxClientes.removeAllItems();
        cbxClientes.addItem("Todos");
        gestorListaCocina.getListaCliente().lisaCliente(cbxClientes);
        obtenerDatosTabla("");
        obtenerTotal();
    }

    private void obtenerTotal() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        lblVenta.setText("Venta total: " + gestorListaCocina.costoReporte(modelo, 3));
        lblTotal.setText("Total: " + modelo.getRowCount());
    }

    @Override
    public void update(Observable o, Object arg) {
        if (arg instanceof Boolean) {

            if ((boolean) arg == true && remover == true) {
                int row = jTable1.getSelectedRow();
                gestorListaCocina.getListaCliente().removerCliente(jTable1.getModel().getValueAt(row, 0).toString());
                gestorListaCocina.mensaje("Cliente removido corretamente", "remover", 1);
                remover = false;
                cbxClientes.removeAllItems();
                cbxClientes.addItem("Todos");
                gestorListaCocina.getListaCliente().lisaCliente(cbxClientes);
            }

            obtenerDatosTabla(cbxClientes.getSelectedItem().toString());
            setVisible(true);
        }
    }

