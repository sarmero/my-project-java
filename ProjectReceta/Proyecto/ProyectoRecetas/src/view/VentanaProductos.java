    private void visualizarImagen() {
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            ven.visualizarProducto(jTable1.getValueAt(row, 0) + "", false);
        }
    }

    private void guardarProductos() {
        Producto pro;

        pro = new Producto();

        pro.setNombre(txtProducto.getText());
        pro.setContenido(Double.valueOf(txtMedida.getText()));
        pro.setPrecio(Double.valueOf(txtPrecio.getText()));
        pro.setRuta(gestorListaCocina.copiarFichero(txtProducto.getText(), rutaFile, "ImagenPoducto"));
        gestorListaCocina.getListaProducto().agregarProducto(pro);

        obtenerProductos();

        gestorListaCocina.mensaje("Producto Guardado corretamente", "Producto", 0);
        rutaFile = "";

        txtMedida.setText("");
        txtPrecio.setText("");
        txtProducto.setText("");
        cambios = true;

    }

    private void modificar() {
        Producto pro = new Producto();
        int row = jTable1.getSelectedRow();
        Object[] prod = gestorListaCocina.getListaProducto().obtenerproducto(row);

        pro.setNombre(txtProducto.getText());
        pro.setContenido(Double.valueOf(txtMedida.getText()));
        pro.setPrecio(Double.valueOf(txtPrecio.getText()));
        pro.setCodigo(gestorListaCocina.getListaProducto().codigoProducto((String) prod[0]));

        String ru = (String) prod[4];

        if (!rutaFile.equals("") && !ru.equals(rutaFile)) {
            gestorListaCocina.eliminarFichero(ru);
            pro.setRuta(gestorListaCocina.copiarFichero(txtProducto.getText(), (String) rutaFile, "imageProducto/"));
        } else {
            pro.setRuta(ru);
        }

        rutaFile = "";
        gestorListaCocina.getListaProducto().editarProducto(pro);
        gestorListaCocina.mensaje("Productos Modificado Correctamente", "Producto", 0);
        btnAdd.setText("Guardar");
        jPanel2.setBorder(
                javax.swing.BorderFactory.createTitledBorder(
                        javax.swing.BorderFactory.createLineBorder(
                                new java.awt.Color(98, 50, 35)), "Añadir Producto",
                        javax.swing.border.TitledBorder.DEFAULT_JUSTIFICATION,
                        javax.swing.border.TitledBorder.DEFAULT_POSITION,
                        new java.awt.Font("Dialog", 1, 12),
                        new java.awt.Color(98, 50, 35)
                )
        );

        ban = false;
        jTable1.setEnabled(true);
        btnCancelar.setBackground(new Color(102, 204, 255));
        btnCancelar.setForeground(new Color(98, 50, 35));
        btnCancelar.setText("Limpiar");
        txtMedida.setText("");
        txtPrecio.setText("");
        txtProducto.setText("");
        obtenerProductos();
        cambios = true;
    }

