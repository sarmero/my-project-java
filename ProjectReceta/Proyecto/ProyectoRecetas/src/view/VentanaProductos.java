    private void editarProducto() {
        int row = jTable1.getSelectedRow();
        Object[] pro = gestorListaCocina.getListaProducto().obtenerproducto(row);

        jPanel2.setBorder(
                javax.swing.BorderFactory.createTitledBorder(
                        javax.swing.BorderFactory.createLineBorder(
                                new java.awt.Color(98, 50, 35)), "Modificar Producto",
                        javax.swing.border.TitledBorder.DEFAULT_JUSTIFICATION,
                        javax.swing.border.TitledBorder.DEFAULT_POSITION,
                        new java.awt.Font("Dialog", 1, 12),
                        new java.awt.Color(98, 50, 35)
                )
        );

        txtMedida.setText(pro[1] + "");
        txtPrecio.setText(pro[2] + "");
        txtProducto.setText(pro[0] + "");
        btnCancelar.setText("Cancelar");
        btnCancelar.setBackground(new Color(255, 153, 153));
        btnCancelar.setForeground(new Color(98, 50, 35));

        String ru = pro[4] + "";
        rutaFile = ru;
        gestorListaCocina.visualizadorImagen(lblImagen, ru);
    }

    @Override
    public void update(Observable o, Object arg) {
        if (arg instanceof Boolean) {

            if ((boolean) arg == true) {
                if (removerAll != true) {
                    int row = jTable1.getSelectedRow();
                    if (jTable1.getSelectedRowCount() > 0) {
                        gestorListaCocina.getListaProducto().eliminarProducto(jTable1.getModel().getValueAt(row, 0) + "");
                        gestorListaCocina.mensaje("Producto removido correctamente", "Remover producto", 1);
                        obtenerProductos();
                        cambios = true;
                    }
                } else {
                    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
                    modelo.setRowCount(0);
                    jTable1.setModel(modelo);
                    gestorListaCocina.getListaProducto().eliminarAllProducto();
                    gestorListaCocina.mensaje("Productos removidos correctamente", "Remover producto", 1);
                    removerAll = false;
                }

            }

        }
    }

